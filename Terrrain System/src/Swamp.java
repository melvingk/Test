
public  class Swamp

{
	public Swamp(int distance, int depth)
 {
		this.distance = distance;
		this.depth = depth;
 }
	private int distance;
	private int depth;

	
	public int getDepth() {
		return depth;
	}

	public void setDepth(int depth) {
		this.depth = depth;
	}

	
	public int getDistance() {
		return distance;
	}

	public void setDistance(int distance) {
		this.distance = distance;
	}



}